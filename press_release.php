					<h3><strong>GoalFace  to Celebrate 11/11/11 With The Launch of Football Everywhere!</strong></h3>
				
			  <p><em>GoalFace announces the upcoming Beta launch of its Football Everywhere™ services on November 11, 2011 (11/11/11). GoalFace's Football Everywhere services will make it easy for fans to follow professional football everywhere in a personalized format that is engaging, fun and 100% football.</em></p>
              
              <p><strong>WASHINGTON, DC USA. 11/1/2011.</strong> GoalFace (<a href="http://www.goalface.com">www.goalface.com</a>), the premier online destination for global football (soccer) today announced the upcoming Beta launch of its Football Everywhere services.  </p>
              
              <p>GoalFace's Football  Everywhere services will make it easy for the global football community—  including fans, bloggers, the news media, scouts, agents and industry  stakeholders—to keep up with, follow and track the players, teams and leagues  they care about.  </p>
              
              <p>Using the latest web 2.0, social media and syndication technologies, GoalFace’s Football Everywhere services will deliver professional football information, including league and team schedule notifications, scores, live match updates, real-time player alerts from across Europe, the Americas, Africa and Asia, to the global football community via the Internet, email, mobile, social media and other channels—anytime, everywhere, on demand. </p>
              
              <p>"GoalFace brings information on 100,000+ professional team and players from 200+ leagues around the world together in one place in a format that is personalized, engaging, fun and 100% football,” said Co-founder and President John Gakau. “This makes it much easier for members of the global football community to keep their fingers on the pulse of the beautiful game by delivering information about their favorites from leagues around the world when, where and how they want it."</p>
              
              <p>Coinciding with the launch of its Football Everywhere services GoalFace will open member registration to the public, which until now has been by invitation only.</p>
              
              <p>GoalFace is currently available in English.  Spanish and other languages are scheduled to follow soon. To access the GoalFace web site, visit <a href="http://www.goalface.com" target="_blank">www.goalface.com</a>. For more information about GoalFace, Inc, visit <a href="http://www.goalface.org" target="_blank">www.goalface.org</a>.</p>
              
              <p><strong>About GoalFace</strong><br />
              GoalFace.com is an online real-time, micro-community experience for global football (soccer), delivering scores, news, live updates, photos, statistics and other coverage from professional football leagues and tournaments across Europe, the Americas, Africa and Asia.</p>
              
              <p>GoalFace.com is 1,000,000+ pages and is 100% dedicated to football. GoalFace provides the global football community with a place to convene, connect and celebrate their passion for the game in an environment that is informative, fun and easy to use.  Members of the global football community— including fans, bloggers, the news media, scouts, agents and industry stakeholders—can access information about 100,000+ professional players and teams from 200+ leagues and tournaments around the world through the web site or via downloadable widgets, RSS, social media applications and other platforms.  Registered members on the site can personalize the experience to focus on their favorites and receive notifications and alerts for the players, teams and leagues they care about most via email, mobile, Facebook, iGoogle or many of their other favorite places on the Internet—anytime, everywhere, on demand.</p>
